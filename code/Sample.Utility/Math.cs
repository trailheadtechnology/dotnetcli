namespace Sample.Utility
{
    public static class Math
    {
        public static long Add(int x, int y) {
            return (long)x + y;
        }

        public static int Subtract(int x, int y)
        {
            return x - y;
        }

        public static long Multiply(int x, int y)
        {
            return (long)x * y;
        }

        public static float Divide(int x, int y)
        {
            return (float)x / y;
        }
    }
}
